<?
$int_tujuh = 7;
7 = $int_tujuh; //  Error!
?>

<!-- 
Pernyataan pertama ($int_tujuh = 7;)  benar — artinya "nilai 7 ditugaskan ke variabel $int_tujuh".

Pernyataan kedua (7 = $int_tujuh;)  salah — karena angka (literal) tidak bisa dijadikan target assignment.

PHP hanya mengizinkan variabel di sisi kiri operator =.

Jika ditulis sebaliknya, PHP akan menampilkan error syntax.
 -->

